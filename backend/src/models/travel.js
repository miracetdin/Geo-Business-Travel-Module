import mongoose from 'mongoose';

const Schema = mongoose.Schema;

const TravelSchema = new Schema({
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
  },
  price: {
    type: Number,
    required: true,
  },
  photos: [String],
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

const Travel = mongoose.model('travel', TravelSchema);

export default Travel;
